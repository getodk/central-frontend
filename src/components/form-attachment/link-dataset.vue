<template>
  <modal id="form-attachment-link-dataset" :state="state" :hideable="!awaitingResponse"
    backdrop @hide="$emit('hide')">
    <template #title>{{ $t('title') }}</template>
    <template #body>
      <div class="modal-introduction">
        <p>
          <span>{{ $t('introduction[0]') }}</span>
          <sentence-separator/>
          <template v-if="attachment != null && attachment.blobExists">
            {{ $t('introduction[1]') }}
          </template>
        </p>
      </div>
      <div class="modal-actions">
        <button type="button" class="btn btn-link" :aria-disabled="awaitingResponse"
          @click="$emit('hide')">
          {{ $t('action.cancel') }}
        </button>
        <button type="button" class="btn btn-primary btn-link-dataset"
          :aria-disabled="awaitingResponse" @click="link">
          {{ $t('action.link') }} <spinner :state="awaitingResponse"/>
        </button>
      </div>
    </template>
  </modal>
</template>

<script>
import Modal from '../modal.vue';
import Spinner from '../spinner.vue';
import SentenceSeparator from '../sentence-separator.vue';

import useRequest from '../../composables/request';
import { apiPaths } from '../../util/request';
import { noop } from '../../util/util';
import { useRequestData } from '../../request-data';

export default {
  name: 'FormAttachmentLinkDataset',
  components: { Modal, Spinner, SentenceSeparator },
  props: {
    state: {
      type: Boolean,
      default: false
    },
    attachment: Object
  },
  emits: ['hide', 'success'],
  setup() {
    const { form } = useRequestData();
    const { request, awaitingResponse } = useRequest();
    return { form, request, awaitingResponse };
  },
  methods: {
    link() {
      this.request({
        method: 'PATCH',
        url: apiPaths.formAttachment(this.form.projectId, this.form.xmlFormId, true, this.attachment.name),
        data: { dataset: true }
      })
        .then(({ data }) => {
          this.$emit('success', data);
        })
        .catch(noop);
    }
  }
};
</script>

