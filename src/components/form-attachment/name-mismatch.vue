<template>
  <modal :state="state" backdrop hideable @hide="cancel">
    <template #title>
      {{ attachment.exists ? $t('title.replace') : $t('title.upload') }}
    </template>
    <template #body>
      <div class="modal-introduction">
        <i18n-t tag="p" keypath="introduction[0]">
          <template #filename>
            <strong>{{ file.name }}</strong>
          </template>
          <template #attachmentName>
            <strong>{{ attachment.name }}</strong>
          </template>
        </i18n-t>
        <p>{{ $t('introduction[1]') }}</p>
      </div>
      <div class="modal-actions">
        <button class="btn btn-link" type="button" @click="cancel">
          {{ $t('action.noCancel') }}
        </button>
        <button class="btn btn-primary" type="button" @click="confirm">
          {{ $t('action.yesProceed') }}
        </button>
      </div>
    </template>
  </modal>
</template>

<script>
import Modal from '../modal.vue';

export default {
  name: 'FormAttachmentNameMismatch',
  components: { Modal },
  props: {
    state: {
      type: Boolean,
      default: false
    },
    plannedUploads: {
      type: Array,
      required: true
    }
  },
  emits: ['confirm', 'cancel', 'hide'],
  computed: {
    // Returning dummy values for `attachment` and `file` when
    // plannedUploads.length !== 1 in order to simplify the template.
    attachment() {
      return this.plannedUploads.length === 1
        ? this.plannedUploads[0].attachment
        : { name: '', exists: false };
    },
    file() {
      return this.plannedUploads.length === 1
        ? this.plannedUploads[0].file
        : { name: '' };
    }
  },
  methods: {
    confirm() {
      this.$emit('hide');
      this.$emit('confirm');
    },
    cancel() {
      this.$emit('hide');
      this.$emit('cancel');
    }
  }
};
</script>

