<template>
  <form id="submission-comment" @submit.prevent="submit">
    <div v-if="editWithoutComment" role="alert">
      <span class="icon-pencil"></span>{{ $t('editWithoutComment') }}
    </div>
    <div class="form-group">
      <markdown-textarea v-model="body" :default-text="$t('field.comment')"
        :show-footer="editWithoutComment || awaitingResponse" required>
        <button type="submit" class="btn btn-primary"
          :aria-disabled="awaitingResponse">
          {{ $t('action.comment') }} <spinner :state="awaitingResponse"/>
        </button>
      </markdown-textarea>
    </div>
  </form>
</template>

<script>
import MarkdownTextarea from '../markdown/textarea.vue';
import Spinner from '../spinner.vue';

import useRequest from '../../composables/request';
import { apiPaths } from '../../util/request';
import { noop } from '../../util/util';
import { useRequestData } from '../../request-data';

export default {
  name: 'SubmissionComment',
  components: { Spinner, MarkdownTextarea },
  props: {
    projectId: {
      type: String,
      required: true
    },
    xmlFormId: {
      type: String,
      required: true
    },
    instanceId: {
      type: String,
      required: true
    },
    feed: Array
  },
  emits: ['success'],
  setup() {
    const { currentUser } = useRequestData();
    const { request, awaitingResponse } = useRequest();
    return { currentUser, request, awaitingResponse };
  },
  data() {
    return {
      body: ''
    };
  },
  computed: {
    editWithoutComment() {
      if (this.feed == null) return false;
      for (const entry of this.feed) {
        if (entry.actorId === this.currentUser.id) {
          if (entry.body != null) return false;
          if (entry.action === 'submission.update.version') return true;
        }
      }
      return false;
    }
  },
  methods: {
    submit() {
      this.request({
        method: 'POST',
        url: apiPaths.submissionComments(
          this.projectId,
          this.xmlFormId,
          this.instanceId
        ),
        data: { body: this.body }
      })
        .then(() => {
          this.$emit('success');
          this.body = '';
        })
        .catch(noop);
    }
  }
};
</script>

