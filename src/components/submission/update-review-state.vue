<template>
  <modal id="submission-update-review-state" :state="state"
    :hideable="!awaitingResponse" backdrop @hide="$emit('hide')">
    <template #title>{{ $t('title') }}</template>
    <template #body>
      <form @submit.prevent="submit">
        <div class="row">
          <div class="col-xs-4">
            <div v-for="reviewState of selectableStates" :key="reviewState"
              class="radio">
              <label>
                <input v-model="selectedState" type="radio"
                  :value="reviewState">
                <span :class="reviewStateIcon(reviewState)"></span>
                <span>{{ $t(`reviewState.${reviewState}`) }}</span>
              </label>
            </div>
          </div>
          <div class="col-xs-8">
            <div class="form-group">
              <markdown-textarea v-model="notes" :default-text="$t('field.notes')"
              :show-footer="awaitingResponse"/>
            </div>
          </div>
        </div>
        <div class="modal-actions">
          <button type="submit" class="btn btn-primary"
            :disabled="awaitingResponse">
            {{ $t('action.update') }} <spinner :state="awaitingResponse"/>
          </button>
          <button type="button" class="btn btn-link"
            :disabled="awaitingResponse" @click="$emit('hide')">
            {{ $t('action.neverMind') }}
          </button>
        </div>
      </form>
    </template>
  </modal>
</template>

<script>
import Modal from '../modal.vue';
import Spinner from '../spinner.vue';
import MarkdownTextarea from '../markdown/textarea.vue';

import request from '../../mixins/request';
import useReviewState from '../../composables/review-state';
import { apiPaths } from '../../util/request';
import { noop } from '../../util/util';

const selectableStates = ['approved', 'hasIssues', 'rejected'];

export default {
  name: 'SubmissionUpdateReviewState',
  components: { Modal, Spinner, MarkdownTextarea },
  mixins: [request()],
  props: {
    state: Boolean,
    projectId: {
      type: String,
      required: true
    },
    xmlFormId: {
      type: String,
      required: true
    },
    submission: Object
  },
  emits: ['hide', 'success'],
  setup() {
    const { reviewStateIcon } = useReviewState();
    return { reviewStateIcon };
  },
  data() {
    return {
      awaitingResponse: false,
      selectedState: 'approved',
      notes: ''
    };
  },
  computed: {
    selectableStates() {
      return selectableStates;
    }
  },
  watch: {
    state(state) {
      if (state) {
        const currentState = this.submission.__system.reviewState;
        this.selectedState = selectableStates.includes(currentState)
          ? currentState
          : 'approved';
        this.$nextTick(() => {
          this.$el.querySelector('input:checked').focus();
        });
      } else {
        this.notes = '';
      }
    }
  },
  methods: {
    submit() {
      const headers = {};
      if (this.notes !== '')
        headers['X-Action-Notes'] = encodeURIComponent(this.notes);
      this.request({
        method: 'PATCH',
        url: apiPaths.submission(
          this.projectId,
          this.xmlFormId,
          this.submission.__id
        ),
        data: { reviewState: this.selectedState },
        headers
      })
        .then(() => {
          // It is the responsibility of the parent component to update the
          // submission.
          this.$emit('success', this.submission, this.selectedState);
        })
        .catch(noop);
    }
  }
};
</script>

