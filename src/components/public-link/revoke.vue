<template>
  <modal id="public-link-revoke" :state="state" :hideable="!awaitingResponse"
    backdrop @hide="$emit('hide')">
    <template #title>{{ $t('title') }}</template>
    <template #body>
      <div class="modal-introduction">
        <p>{{ $t('introduction[0]') }}</p>
        <p><strong>{{ $t('common.areYouSure') }}</strong></p>
      </div>
      <div class="modal-actions">
        <button type="button" class="btn btn-link" :aria-disabled="awaitingResponse"
          @click="$emit('hide')">
          {{ $t('action.noCancel') }}
        </button>
        <button type="button" class="btn btn-danger"
          :aria-disabled="awaitingResponse" @click="revoke">
          {{ $t('action.yesProceed') }} <spinner :state="awaitingResponse"/>
        </button>
      </div>
    </template>
  </modal>
</template>

<script setup>
import Modal from '../modal.vue';
import Spinner from '../spinner.vue';

import useRequest from '../../composables/request';
import { apiPaths } from '../../util/request';
import { noop } from '../../util/util';

defineOptions({
  name: 'PublicLinkRevoke'
});
const props = defineProps({
  state: Boolean,
  publicLink: Object
});
const emit = defineEmits(['hide', 'success']);

const { request, awaitingResponse } = useRequest();
const revoke = () => {
  request({ method: 'DELETE', url: apiPaths.session(props.publicLink.token) })
    .then(() => {
      emit('success', props.publicLink);
    })
    .catch(noop);
};
</script>

