<template>
  <modal id="user-reset-password" :state="state" :hideable="!awaitingResponse"
    backdrop @hide="$emit('hide')">
    <template #title>{{ $t('title') }}</template>
    <template #body>
      <i18n-t v-if="user != null" tag="p" keypath="introduction.full"
        class="modal-introduction">
        <template #resetPassword>
          <strong>{{ $t('introduction.resetPassword') }}</strong>
        </template>
        <template #displayName>{{ user.displayName }}</template>
        <template #email>{{ user.email }}</template>
      </i18n-t>
      <div class="modal-actions">
        <button type="button" class="btn btn-primary"
          :disabled="awaitingResponse" @click="resetPassword">
          {{ $t('action.resetPassword') }} <spinner :state="awaitingResponse"/>
        </button>
        <button type="button" class="btn btn-link" :disabled="awaitingResponse"
          @click="$emit('hide')">
          {{ $t('action.close') }}
        </button>
      </div>
    </template>
  </modal>
</template>

<script>
import Modal from '../modal.vue';
import Spinner from '../spinner.vue';

import request from '../../mixins/request';
import { noop } from '../../util/util';

export default {
  name: 'UserResetPassword',
  components: { Modal, Spinner },
  mixins: [request()],
  props: {
    state: Boolean,
    user: Object
  },
  emits: ['hide', 'success'],
  data() {
    return {
      awaitingResponse: false
    };
  },
  methods: {
    resetPassword() {
      this.request({
        method: 'POST',
        url: '/v1/users/reset/initiate?invalidate=true',
        data: { email: this.user.email }
      })
        .then(() => {
          this.$emit('success', this.user);
        })
        .catch(noop);
    }
  }
};
</script>

