export default {
  name: 'FormVersionString',
  props: {
    version: {
      type: String,
      required: true
    }
  },
  computed: {
    htmlClass() {
      return this.version !== ''
        ? 'form-version-string'
        : 'form-version-string blank-version';
    },
    versionOrBlank() {
      return this.version !== '' ? this.version : this.$t('blank');
    }
  }
};
</script>

